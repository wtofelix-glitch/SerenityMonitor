# scan_mainboard — 主板全量扫描
# ============================================================

def scan_mainboard(top_n: int = 10) -> list[dict]:
    """
    全量扫描主板：获取全量主板股票列表，对每只评分，返回 TOP N 候选

    Parameters
    ----------
    top_n : int
        返回排名前 N 的候选标的 (默认 10)

    Returns
    -------
    list[dict]
        按综合评分降序排列，每个元素含:
          rank, code, name, total_score, serenity_score,
          factor_score, fundamental_score, industry, suggestion,
          price, change_pct
    """
    # 获取全量主板列表
    mainboard_stocks = get_mainboard_list()
    if not mainboard_stocks:
        print("❌ 无法获取任何主板股票数据")
        return []

    total_count = len(mainboard_stocks)
    print(f"📊 共获取到 {total_count} 只主板标的，开始评分...")
    print()

    # 建立 code → snapshot 索引
    snap_map = {s["code"]: s for s in mainboard_stocks}

    # 获取已有评分（针对 STOCK_MAP 中的标的）
    known_codes = [c for c in snap_map if c in STOCK_MAP]
    new_codes = [c for c in snap_map if c not in STOCK_MAP]

    # 对已知标的用现有评分体系
    known_results = []
    if known_codes:
        try:
            from scorer import score_all
            all_scores = score_all()
            for s in all_scores:
                code = s["code"]
                if code in snap_map:
                    known_results.append({
                        "code": code,
                        "name": s.get("name", snap_map[code].get("name", code)),
                        "total_score": s.get("total_score", 0),
                        "serenity_score": s.get("serenity_score", 0),
                        "factor_score": s.get("factor_score", 0),
                        "fundamental_score": None,
                        "industry": "",
                        "suggestion": _suggestion_from_score(s.get("total_score", 0)),
                        "price": s.get("close", snap_map[code].get("close", 0)),
                        "change_pct": s.get("change_pct", snap_map[code].get("change_pct", 0)),
                    })
        except Exception as e:
            print(f"⚠️ 已知标的评分失败: {e}")
            # 降级：用数据库中的最新评分
            db_scores = get_latest_scores(known_codes)
            db_map = {s["code"]: s for s in db_scores}
            for code in known_codes:
                snap = snap_map.get(code, {})
                ds = db_map.get(code, {})
                total = ds.get("total_score", 0) or 0
                known_results.append({
                    "code": code,
                    "name": STOCK_MAP.get(code, {}).get("name", snap.get("name", code)),
                    "total_score": total,
                    "serenity_score": ds.get("serenity_score", 0) or 0,
                    "factor_score": ds.get("factor_score", 0) or 0,
                    "fundamental_score": None,
                    "industry": "",
                    "suggestion": _suggestion_from_score(total),
                    "price": snap.get("close", 0),
                    "change_pct": snap.get("change_pct", 0),
                })

    # 对新标的快速评分
    new_results = []
    batch_size = 50
    total_new = len(new_codes)
    for i in range(0, total_new, batch_size):
        batch = new_codes[i:i + batch_size]
        for code in batch:
            snap = snap_map.get(code, {})
            try:
                result = _quick_score(code, snap)
                new_results.append(result)
            except Exception as e:
                continue
        # 进度提示（全量扫描时每批一次）
        if total_new > 100:
            pct = min(100, int((i + batch_size) / total_new * 100))
            print(f"  ⏳ 评分进度: {pct}% ({min(i+batch_size, total_new)}/{total_new})", end="\r")

    if total_new > 100:
        print()  # 换行

    # 合并结果
    all_results = known_results + new_results

    # 按总评分降序排列
    all_results.sort(key=lambda x: x["total_score"], reverse=True)

    # 取 TOP N
    top = all_results[:top_n]

    # 编号
    for i, r in enumerate(top):
        r["rank"] = i + 1

    print(f"\n✅ 评分完成！共评分 {len(known_results)} 只已知标的 + {len(new_results)} 只新标的")
    print(f"🏆 TOP{top_n} 候选如下：\n")

    return top


def _suggestion_from_score(score: float) -> str:
    if score >= 82:
        return "🟢🟢🟢 强力买入"
    elif score >= 72:
        return "🟢🟢 买入"
    elif score >= 62:
        return "🟢 关注"
    elif score >= 50:
        return "⚪ 观察"
    elif score >= 35:
        return "🟡 谨慎"
    else:
        return "🔴 回避"


