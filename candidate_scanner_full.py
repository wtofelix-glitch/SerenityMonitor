def _fetch_via_preset() -> list[dict]:
    """使用预设主板列表（~2800只主板代码）"""
    try:
        from data_engine import fetch_realtime, parse_sina_line, SINA_PREFIX
        import urllib.request

        # 按批次查询新浪（每批最多100只）
        batch_size = 80
        all_results = []
        codes = _PRESET_MAINBOARD_CODES

        for i in range(0, len(codes), batch_size):
            batch = codes[i:i + batch_size]
            try:
                # 构建新浪URL
                sina_codes = []
                for c in batch:
                    if c.startswith("6"):
                        sina_codes.append(f"sh{c}")
                    else:
                        sina_codes.append(f"sz{c}")
                url = SINA_PREFIX + ",".join(sina_codes)
                proxy_handler = urllib.request.ProxyHandler({})
                opener = urllib.request.build_opener(proxy_handler)
                req = urllib.request.Request(url)
                req.add_header("Referer", "https://finance.sina.com.cn")
                with opener.open(req, timeout=15) as resp:
                    raw = resp.read().decode("gbk")

                for line in raw.strip().split("\n"):
                    parsed = parse_sina_line(line)
                    if parsed:
                        code = parsed["code"]
                        close_y = parsed.get("close_yesterday", 0) or 0
                        price = parsed.get("price", 0) or 0
                        change_pct = round((price - close_y) / close_y * 100, 2) if close_y > 0 else 0
                        all_results.append({
                            "code": code,
                            "name": parsed.get("name", ""),
                            "close": price,
                            "change_pct": change_pct,
                            "open": parsed.get("open", 0),
                            "high": parsed.get("high", 0),
                            "low": parsed.get("low", 0),
                            "volume": parsed.get("volume", 0),
                            "amount": parsed.get("amount", 0),
                        })
            except Exception as e:
                logger.warning(f"批 {i//batch_size+1} 获取失败: {e}")
                continue
            time.sleep(0.1)  # 避免请求过快

        return all_results
    except Exception as e:
        logger.warning(f"预设列表获取失败: {e}")
        return []


