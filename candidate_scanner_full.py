# 方案A: AKShare 全A股行情
# ============================================================
def _fetch_via_akshare() -> list[dict]:
    """使用 AKShare 获取全A股实时行情，过滤主板"""
    try:
        import akshare as ak
        df = ak.stock_zh_a_spot_em()
        results = []
        for _, row in df.iterrows():
            code = str(row.get("代码", "")).strip().zfill(6)
            if _is_mainboard(code):
                results.append({
                    "code": code,
                    "name": row.get("名称", ""),
                    "close": float(row.get("最新价", 0) or 0),
                    "change_pct": float(row.get("涨跌幅", 0) or 0),
                    "open": float(row.get("今开", 0) or 0),
                    "high": float(row.get("最高", 0) or 0),
                    "low": float(row.get("最低", 0) or 0),
                    "volume": float(row.get("成交量", 0) or 0),
                    "amount": float(row.get("成交额", 0) or 0),
                    "pe_ttm": float(row.get("市盈率-动态", 0) or 0),
                    "total_mv": float(row.get("总市值", 0) or 0),
                })
        return results
    except ImportError:
        logger.warning("AKShare 未安装，跳过方案A")
        return []
    except Exception as e:
        logger.warning(f"AKShare 获取失败: {e}")
        return []


# ============================================================
# 方案B: 从新浪批量拉取沪深主板列表
# ============================================================
def _fetch_via_sina_batch() -> list[dict]:
    """
    使用新浪财经的分页接口获取沪深主板列表
    通过爬取板块成分股页面来获取主板股票列表
    """
    try:
        import urllib.request
        import json
        import re

        results = []
        # 上证主板代码范围: 600000-605999, 但实际只拉取存在的
        # 深证主板代码: 000001-001999, 002000-002999

        # 使用沪深A股列表页
        sh_url = "https://vip.stock.finance.sina.com.cn/quotes_service/api/json_v2.php/Market_Center.getHQNodeData?page=1&num=3000&sort=code&asc=1&node=sh_a&symbol=&_s_r_a=page"
        sz_url = "https://vip.stock.finance.sina.com.cn/quotes_service/api/json_v2.php/Market_Center.getHQNodeData?page=1&num=3000&sort=code&asc=1&node=sz_a&symbol=&_s_r_a=page"

        proxy_handler = urllib.request.ProxyHandler({})
        opener = urllib.request.build_opener(proxy_handler)

        for url, market_prefix in [(sh_url, "sh"), (sz_url, "sz")]:
            try:
                req = urllib.request.Request(url)
                req.add_header("Referer", "https://finance.sina.com.cn")
                with opener.open(req, timeout=15) as resp:
                    raw = resp.read().decode("gbk")
                # 解析 JSON
                data = json.loads(raw)
                for item in data:
                    code = str(item.get("code", "")).strip().zfill(6)
                    if _is_mainboard(code):
                        results.append({
                            "code": code,
                            "name": item.get("name", ""),
                            "close": float(item.get("trade", 0) or 0),
                            "change_pct": float(item.get("changepercent", 0) or 0),
                            "open": float(item.get("open", 0) or 0),
                            "high": float(item.get("high", 0) or 0),
                            "low": float(item.get("low", 0) or 0),
                            "volume": float(item.get("volume", 0) or 0),
                            "amount": float(item.get("amount", 0) or 0),
                            "pe_ttm": float(item.get("pe", 0) or 0),
                            "total_mv": float(item.get("mktcap", 0) or 0),
                        })
            except Exception as e:
                logger.warning(f"新浪 {market_prefix} 列表获取失败: {e}")
                continue

        return results
    except ImportError:
        return []
    except Exception as e:
        logger.warning(f"新浪批量获取失败: {e}")
        return []


