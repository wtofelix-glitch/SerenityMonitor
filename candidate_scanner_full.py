def scan_from_list(code_list: list[str], top_n: int = 10) -> list[dict]:
    """
    从给定的股票代码列表扫描评分

    Parameters
    ----------
    code_list : list[str]
        要扫描的股票代码列表
    top_n : int
        返回排名前 N 的候选 (默认 10)

    Returns
    -------
    list[dict]
    """
    # 过滤主板
    codes = [c.strip().zfill(6) for c in code_list if _is_mainboard(c.strip().zfill(6))]
    if not codes:
        print("❌ 给定列表中无有效主板标的")
        return []

    print(f"📡 从给定列表扫描 {len(codes)} 只主板标的...")

    # 从新浪获取实时数据
    from data_engine import fetch_realtime
    snapshots = fetch_realtime(codes)
    snap_map = {s["code"]: s for s in snapshots}

    # 分类已知/新标的
    known_codes = [c for c in codes if c in STOCK_MAP and c in snap_map]
    new_codes = [c for c in codes if c not in STOCK_MAP and c in snap_map]

    results = []

    # 已知标的评分
    if known_codes:
        try:
            from scorer import score_all
            all_scores = score_all()
            for s in all_scores:
                if s["code"] in known_codes:
                    results.append({
                        "code": s["code"],
                        "name": s.get("name", snap_map[s["code"]].get("name", s["code"])),
                        "total_score": s.get("total_score", 0),
                        "serenity_score": s.get("serenity_score", 0),
                        "factor_score": s.get("factor_score", 0),
                        "fundamental_score": None,
                        "industry": "",
                        "suggestion": _suggestion_from_score(s.get("total_score", 0)),
                        "price": s.get("close", snap_map[s["code"]].get("close", 0)),
                        "change_pct": s.get("change_pct", snap_map[s["code"]].get("change_pct", 0)),
                    })
        except Exception:
            for code in known_codes:
                snap = snap_map.get(code, {})
                results.append({
                    "code": code,
                    "name": snap.get("name", code),
                    "total_score": 0,
                    "serenity_score": 0,
                    "factor_score": 0,
                    "fundamental_score": None,
                    "industry": "",
                    "suggestion": "⚪ 数据不足",
                    "price": snap.get("close", 0),
                    "change_pct": snap.get("change_pct", 0),
                })

    # 新标的快速评分
    for code in new_codes:
        snap = snap_map.get(code, {})
        try:
            result = _quick_score(code, snap)
            results.append(result)
        except Exception:
            continue

    # 排序
    results.sort(key=lambda x: x["total_score"], reverse=True)
    top = results[:top_n]
    for i, r in enumerate(top):
        r["rank"] = i + 1

    return top


