def format_scan_results(results: list[dict]) -> str:
    """将扫描结果格式化为可读文本"""
    if not results:
        return "📭 无符合条件的主板候选标的"

    lines = [
        f"📊 主板全量扫描 TOP{len(results)} | {date.today()}",
        "═" * 80,
        f"{'排名':>4} {'代码':>6} {'名称':<8} {'总分':>6} {'Serenity':>8} {'因子':>6} {'基本面':>8} {'行业':<10} {'建议':<14}",
        "─" * 80,
    ]

    for r in results:
        rank = r.get("rank", 0)
        code = r.get("code", "")
        name = r.get("name", "")[:8]
        total = r.get("total_score", 0)
        serenity = r.get("serenity_score", 0)
        factor = r.get("factor_score", 0)
        fund = r.get("fundamental_score")
        fund_str = f"{fund:+.3f}" if fund is not None else "N/A"
        industry = r.get("industry", "") or ""
        suggestion = r.get("suggestion", "")

        lines.append(
            f"{rank:>4} {code:>6} {name:<8} {total:>6.1f} {serenity:>8.1f} "
            f"{factor:>6.1f} {fund_str:>8} {industry:<10} {suggestion:<14}"
        )

    lines.append("═" * 80)
    lines.append("💡 以上为算法建议，最终由用户手动确认是否加入 STOCK_MAP")

    return "\n".join(lines)


