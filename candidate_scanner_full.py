# ============================================================
# 对单个标的进行快速评分（用于新发现的主板股）
# ============================================================

def _quick_score(code: str, snapshot: Optional[dict] = None) -> dict:
    """
    对单个标的快速评分（用于非 STOCK_MAP 的新发现标的）

    Returns
    -------
    dict
        code, name, total_score, serenity_score, factor_score,
        fundamental_score, industry, suggestion
    """
    name = (snapshot or {}).get("name", code)
    price = (snapshot or {}).get("close", 0) or 0
    change_pct = (snapshot or {}).get("change_pct", 0) or 0

    # 1. 检查历史评分
    db_scores = get_latest_scores([code])
    db_total = 0
    if db_scores:
        db_total = db_scores[0].get("total_score", 0) or 0

    # 2. Alpha 因子信号
    factor_strength = 0.0
    try:
        factors = _factor_engine.compute_all_factors(code)
        signals = factors.get("signals", {})
        vals = [float(v) for v in signals.values() if v is not None]
        factor_strength = sum(vals) / len(vals) if vals else 0.0
    except Exception:
        pass

    # 因子评分: factor_strength ∈ [-1,1] → [0,100]
    factor_score = max(0, min(100, 50 + factor_strength * 50))

    # 3. 基本面信号
    fundamental_signal = None
    try:
        fundamental_signal = _fund_engine.get_fundamental_signal(code)
    except Exception:
        pass

    # 基本面评分: signal ∈ [-1,1] → [0,100]
    fund_score = 50
    if fundamental_signal is not None:
        fund_score = max(0, min(100, 50 + fundamental_signal * 50))

    # 4. 动量评分（基于涨跌幅）
    if change_pct < -3:
        momentum_score = 70  # 大跌可能是机会
    elif change_pct < 0:
        momentum_score = 85  # 温和回调
    elif change_pct < 2:
        momentum_score = 65  # 微涨
    elif change_pct < 5:
        momentum_score = 50  # 上涨中
    else:
        momentum_score = 35  # 大涨后风险

    # 5. 综合评分（无 Serenity 维度时用因子+基本面+动量）
    if db_total > 0:
        # 若有历史评分，融合使用
        total_score = db_total * 0.5 + factor_score * 0.25 + fund_score * 0.25
    else:
        total_score = factor_score * 0.40 + fund_score * 0.30 + momentum_score * 0.30

    total_score = round(total_score, 1)

    # 6. 行业（暂缺，留空）
    industry = ""

    # 7. 建议
    if total_score >= 75:
        suggestion = "🟢🟢 买入"
    elif total_score >= 65:
        suggestion = "🟢 关注"
    elif total_score >= 50:
        suggestion = "⚪ 观察"
    elif total_score >= 35:
        suggestion = "🟡 谨慎"
    else:
        suggestion = "🔴 回避"

    return {
        "code": code,
        "name": name,
        "total_score": total_score,
        "serenity_score": 0,  # 新标的无 Serenity 维度
        "factor_score": round(factor_score, 1),
        "fundamental_score": round(fundamental_signal, 4) if fundamental_signal is not None else None,
        "industry": industry,
        "suggestion": suggestion,
        "factor_strength": round(factor_strength, 4),
        "price": price,
        "change_pct": change_pct,
    }


